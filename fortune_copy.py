def year_copy(ten_god):
    """세운 문구. 십성이 없거나 모르는 값이면 안전한 기본값."""
    return {
        "title": YEAR_TITLE.get(ten_god) or "흐름을 살피는 해",
        "tone": YEAR_TONE.get(ten_god) or "",
    }


def month_copy(ten_god):
    """월운 문구. 십성이 없거나 모르는 값이면 안전한 기본값."""
