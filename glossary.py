def _build():
    seen = set()
    out = []
    for key, hanja, short, category in _TERMS:
        assert key not in seen, "용어 키 중복: %s" % key
        seen.add(key)
        out.append({
            "key": key,        # 엔진이 chart 응답에서 실제로 내보내는 문자열
            "name": key,       # 화면에 띄울 한글명(지금은 key 와 같지만, 나중에 달라질 수 있어 분리)
            "hanja": hanja,
            "short": short,
            "category": category,
        })
    return out


